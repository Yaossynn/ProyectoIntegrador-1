package com.grupo4.mbintegrador.model;

public class MenuWeb {
    private int imenuId;
    private String vmenNomb;
    private String vmenIcon;
    private int vmenEsta;
    private String vmenRefe;

    // Getters y Setters
    public int getImenuId() {
        return imenuId;
    }

    public void setImenuId(int imenuId) {
        this.imenuId = imenuId;
    }

    public String getVmenNomb() {
        return vmenNomb;
    }

    public void setVmenNomb(String vmenNomb) {
        this.vmenNomb = vmenNomb;
    }

    public String getVmenIcon() {
        return vmenIcon;
    }

    public void setVmenIcon(String vmenIcon) {
        this.vmenIcon = vmenIcon;
    }

    public int getVmenEsta() {
        return vmenEsta;
    }

    public void setVmenEsta(int vmenEsta) {
        this.vmenEsta = vmenEsta;
    }

    public String getVmenRefe() {
        return vmenRefe;
    }

    public void setVmenRefe(String vmenRefe) {
        this.vmenRefe = vmenRefe;
    }
}
