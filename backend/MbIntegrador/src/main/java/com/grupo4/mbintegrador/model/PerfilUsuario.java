package com.grupo4.mbintegrador.model;

public class PerfilUsuario {
    private int iperfId;
    private String vperfNomb;
    private int iperfEsta;

    // Getters y Setters
    public int getIperfId() {
        return iperfId;
    }

    public void setIperfId(int iperfId) {
        this.iperfId = iperfId;
    }

    public String getVperfNomb() {
        return vperfNomb;
    }

    public void setVperfNomb(String vperfNomb) {
        this.vperfNomb = vperfNomb;
    }

    public int getIperfEsta() {
        return iperfEsta;
    }

    public void setIperfEsta(int iperfEsta) {
        this.iperfEsta = iperfEsta;
    }
}
