package com.grupo4.mbintegrador.model;

import java.util.Date;

public class Observado {
    private int iobsId;
    private String vobsNomb;
    private String vobsNomc;
    private int iobsEsta;
    private String vobsUsuc;
    private String vobsUsum;
    private Date dobsFecc;
    private Date dobsFecm;

    // Getters y Setters
    public int getIobsId() {
        return iobsId;
    }

    public void setIobsId(int iobsId) {
        this.iobsId = iobsId;
    }

    public String getVobsNomb() {
        return vobsNomb;
    }

    public void setVobsNomb(String vobsNomb) {
        this.vobsNomb = vobsNomb;
    }

    public String getVobsNomc() {
        return vobsNomc;
    }

    public void setVobsNomc(String vobsNomc) {
        this.vobsNomc = vobsNomc;
    }

    public int getIobsEsta() {
        return iobsEsta;
    }

    public void setIobsEsta(int iobsEsta) {
        this.iobsEsta = iobsEsta;
    }

    public String getVobsUsuc() {
        return vobsUsuc;
    }

    public void setVobsUsuc(String vobsUsuc) {
        this.vobsUsuc = vobsUsuc;
    }

    public String getVobsUsum() {
        return vobsUsum;
    }

    public void setVobsUsum(String vobsUsum) {
        this.vobsUsum = vobsUsum;
    }

    public Date getDobsFecc() {
        return dobsFecc;
    }

    public void setDobsFecc(Date dobsFecc) {
        this.dobsFecc = dobsFecc;
    }

    public Date getDobsFecm() {
        return dobsFecm;
    }

    public void setDobsFecm(Date dobsFecm) {
        this.dobsFecm = dobsFecm;
    }
}
